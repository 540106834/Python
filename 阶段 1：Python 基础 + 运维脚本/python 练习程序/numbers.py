# 1. 定义两个变量 `a=15` 和 `b=4`，计算并打印：

#    * 和
#    * 差
#    * 乘积
#    * 浮点除法
#    * 整除
#    * 取余
#    * 幂运算

a=15
b=6

print("a + b =", a + b)
print("a - b =", a - b) 
print("a * b =", a * b)
print("a / b =", float(a / b))
print("a // b =", a // b)
print("a % b =", a % b)
print("a ** b =", a ** b)